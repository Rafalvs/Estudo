"""
8 - Elaborar um algoritmo que leia três números some 10 ao primeiro, subtraia
cinco do segundo e multiplique o terceiro por dois, após efetuar estas
operações mostre o resultado das operações.
"""

x = int(input("Digite um numero: "))
y = int(input("Digite outro numero: "))
